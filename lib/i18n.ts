export type TranslationKey = keyof typeof translations.ar;

export function getTranslations(lang: Language) {
  return translations[lang];
}

export function getCategoryName(categoryId: string, lang: Language): string {
  const map: Record<string, TranslationKey> = {
    food: 'catFood',
    transport: 'catTransport',
    bills: 'catBills',
    shopping: 'catShopping',
    health: 'catHealth',
    education: 'catEducation',
    entertainment: 'catEntertainment',
    rent: 'catRent',
    phone: 'catPhone',
    clothes: 'catClothes',
    other_expense: 'catOther',
    salary: 'catSalary',
    freelance: 'catFreelance',
    investment: 'catInvestment',
    gift: 'catGift',
    bonus: 'catBonus',
    other_income: 'catOther',
  };
  const key = map[categoryId];
  if (key) return translations[lang][key] as string;
  return categoryId;
}

export function getWalletIconLabel(icon: string, lang: Language): string {
  const map: Record<string, TranslationKey> = {
    'account-balance-wallet': 'walletIconWallet',
    'account-balance': 'walletIconBank',
    'savings': 'walletIconSavings',
    'credit-card': 'walletIconCard',
    'attach-money': 'walletIconCash',
    'business-center': 'walletIconBusiness',
  };
  const key = map[icon];
  if (key) return translations[lang][key] as string;
  return icon;
}

export function getCurrencyName(code: string, lang: Language): string {
  const map: Record<string, TranslationKey> = {
    EGP: 'egp',
    KWD: 'kwd',
    USD: 'usd',
  };
  const key = map[code];
  if (key) return translations[lang][key] as string;
  return code;
}

export function formatDateLocalized(dateStr: string, lang: Language): string {
  const date = new Date(dateStr);
  const t = translations[lang];
  return `${date.getDate()} ${t.months[date.getMonth()]} ${date.getFullYear()}`;
}
